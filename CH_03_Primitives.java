public class CH_03_Primitives {
    //Primitive is data type cann not be break further
    public static void main(String[] args) {
        byte tnew =12;
        System.out.println(tnew);

        short onew$3y=129;
        System.out.println(onew$3y);

        int rollno=64567;
        System.out.println(rollno);

        float thnew = 1.0f;
        System.out.println(thnew);

        long fonew = 91212;
        System.out.println(fonew);

        double finew = 8.07d;
        System.out.println(finew);

        char sinew = 'y';
        System.out.println(sinew);

        boolean abc=true;
        System.out.println(abc);

    }
}

